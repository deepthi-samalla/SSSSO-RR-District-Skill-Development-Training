num = int(input("Enter a number: "))  #Input is given by user

if num > 0:         #Checks if the number is greater than 0
    print("Positive number")    #Prints if the number is positive
elif num < 0:       #Checks if the number is less than 0
    print("Negative number")    #Prints if the number is negetive
else:               #Checks if the number is equal to 0
    print("Zero")               #Prints if the number is zero
#Output:
# Enter a number: 10
# Positive number
# Enter a number: -5
# Negetive number
# Enter a number: 0
# Zero