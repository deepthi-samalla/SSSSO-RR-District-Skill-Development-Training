a = int(input("Enter first number: "))   #takes input from user
b = int(input("Enter second number: "))   #takes input from user

if a > b:         #Checks if the 1st number is greater than the 2nd number
    print("Largest is:", a)    #prints if the above condition is true
else:              #Checks if the 1st number is not greater than 2nd number
    print("Largest is:", b)    #prints if the else condition is true
#Output:
# Enter first number: 19
# Enter second number:10
# Largest is: 19