# Task: Start with count = 0. Increment it by 1 three times using +=, then print the final value.

# Initialize the count variable
count = 0

# Increment count by 1, three times, using the += operator
count += 1 # count is now 1
count += 1 # count is now 2
count += 1 # count is now 3

# Print the final value of count
print(f"The final count is: {count}")