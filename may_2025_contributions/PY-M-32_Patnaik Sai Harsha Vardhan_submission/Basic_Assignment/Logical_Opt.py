# 5. Logical Operators
a = input("Enter True or False for first value: ") == 'True' 
b = input("Enter True or False for second value: ") == 'True'
print("a and b:", a and b)
print("a or b:", a or b)
print("not a:", not a)
# output: 
# a and b: True if both a and b are True, otherwise False
# a or b: True if either a or b is True, otherwise False
# not a: True if a is False, otherwise False
