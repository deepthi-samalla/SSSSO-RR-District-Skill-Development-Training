#Difference between a stack and queue:    
##Stack:
    #Follows Last In First Out (LIFO)
    #Insertion: push()
    #Removal: pop()
    #Example: Undo functionality
##Queue:
    #Follows First in First Out (FIFO)
    #Insertion: enqueue()
    #Removal: dequeue()
    ##Example: Printer task scheduling
