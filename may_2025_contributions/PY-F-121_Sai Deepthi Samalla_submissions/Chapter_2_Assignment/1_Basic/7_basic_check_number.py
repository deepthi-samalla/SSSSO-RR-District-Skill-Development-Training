# Chapter 2 - Basic Assignment 7: Indentation with if Statement
# Question: Write a program using proper indentation that prints a message if a number is greater than 10.

# Define a number using a variable
# You can change this number to test different cases
number = 15

# Use an if statement to check if the number is greater than 10
# The 'print' line below is indented by 4 spaces (or one tab) to show it belongs to the 'if' block.
if number > 10:
    print(f"The number {number} is greater than 10.") # This line is indented

print("Program finished.") # This line is NOT indented, so it runs regardless of the 'if' condition