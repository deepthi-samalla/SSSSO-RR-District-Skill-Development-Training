# Assignment: Bitwise operators
# Task: Given two integers (e.g., 5 and 3), print their bitwise AND (&) and OR (|).

# Define the two integers directly
num1 = 5
num2 = 3

# Perform and print Bitwise AND and OR operations concisely
print(f"Bitwise AND ({num1} & {num2}): {num1 & num2}")
print(f"Bitwise OR ({num1} | {num2}): {num1 | num2}")