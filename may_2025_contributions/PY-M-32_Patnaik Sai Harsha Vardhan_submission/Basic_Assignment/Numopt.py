# 2. Numeric Operators
a = int(input("Enter first number: ")) # first number input by the user
b = int(input("Enter second number: ")) # second number input by the user
print("Sum:", a + b) # Sum of the two numbers
print("Difference:", a - b) # Difference of the two numbers
print("Product:", a * b) # Product of the two numbers
print("Quotient:", a / b) # Quotient of the two numbers
#output:
# Sum: <sum of a and b>
# Difference: <difference of a and b>
# Product: <product of a and b>
# Quotient: <quotient of a and b>
