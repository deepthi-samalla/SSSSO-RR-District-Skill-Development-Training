#List any three built-in Python modules.

# three built-in Python modules:

#1.math – Provides mathematical functions like sqrt(), pi, sin(), etc.
import math
print(math.sqrt(16))  # Output: 4.0

#2.datetime – Helps you work with dates and times.
import datetime
print(datetime.datetime.now())  # Current date and time

#3.random – Allows you to generate random numbers or make random choices.
import random
print(random.randint(1, 10))  # Random number between 1 and 10
