# Assignment: Numeric operators
# Task: Ask the user for two whole numbers and print their sum, difference, product, and quotient.

num1 = int(input("Enter the first whole number: "))
num2 = int(input("Enter the second whole number: "))

print(f"Sum: {num1 + num2}")
print(f"Difference: {num1 - num2}")
print(f"Product: {num1 * num2}")
print(f"Quotient: {num1 / num2}")